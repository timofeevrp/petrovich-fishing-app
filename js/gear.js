// "Что взять" — короткие практичные советы по одежде/снаряжению под текущую погоду.

export function getGearTips(current) {
  const tips = [];

  if (current.tempAir <= 0) {
    tips.push("Термобельё и тёплые перчатки — на воде холоднее, чем кажется по прогнозу.");
  } else if (current.tempAir <= 10) {
    tips.push("Тёплая куртка и слой потеплее — утро и вечер будут прохладными.");
  } else if (current.tempAir <= 20) {
    tips.push("Лёгкая куртка не помешает — к вечеру может посвежеть.");
  } else {
    tips.push("Лёгкая одежда и головной убор от солнца — днём будет жарко.");
  }

  if (current.precip >= 0.5) {
    tips.push("Дождевик и непромокаемая обувь — без них будет неуютно.");
  }

  if (current.windSpeed >= 8) {
    tips.push("Ветрозащитная куртка и место, закрытое от ветра — сегодня продувает.");
  }

  return tips;
}
